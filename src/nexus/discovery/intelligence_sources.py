class ContentProductionDiscoverySource:
    """Fetches Content Production service status and publishes to Nexus KB.

    The Content Production service is a standalone service (port 8013).
    This source hits its API to surface production status, recent jobs,
    and revenue metrics into the Nexus knowledge base so other services
    (and C-Suite executives via Nexus queries) can discover what the
    production line has been doing.
    """

    def __init__(
        self,
        resource_discovery: ResourceDiscovery,
        knowledge_base: Any,
        cp_service_url: Optional[str] = None,
    ):
        self.resource_discovery = resource_discovery
        self.knowledge_base = knowledge_base
        self.cp_service_url = cp_service_url or os.environ.get(
            "CP_SERVICE_URL", "http://localhost:8013"
        )

        resource_discovery.register_source(ResourceSource.CONTENT_PRODUCTION, self)
        logger.info("ContentProductionDiscoverySource registered")

    def _fetch_json(self, url: str) -> Optional[Dict[str, Any]]:
        """GET url and return parsed JSON, or None on failure."""
        import json
        from urllib.error import URLError
        from urllib.request import Request, urlopen

        try:
            req = Request(url, headers={"Accept": "application/json"})
            with urlopen(req, timeout=5) as resp:
                return json.loads(resp.read().decode("utf-8"))
        except (URLError, OSError) as exc:
            logger.warning("ContentProduction: failed to fetch %s: %s", url, exc)
            return None
        except Exception as exc:
            logger.warning("ContentProduction: unexpected error fetching %s: %s", url, exc)
            return None

    async def discover(self) -> int:
        """Collect production line status into Nexus KB."""
        from nexus.memory.knowledge_base import KnowledgeType

        count = 0

        # Fetch production status
        status = self._fetch_json(f"{self.cp_service_url}/v1/production/status")
        if status:
            active = status.get("active_jobs", 0)
            completed = status.get("completed_jobs", 0)
            queued = status.get("queued_jobs", 0)
            fact = (
                f"Content Production Line status: {active} active, "
                f"{queued} queued, {completed} completed jobs."
            )
            self.knowledge_base.add_knowledge(
                content=fact,
                knowledge_type=KnowledgeType.FACTUAL,
                source="content_production:status",
                confidence=0.9,
                context_tags=["content_production", "status"],
            )
            count += 1

        # Fetch revenue metrics
        revenue = self._fetch_json(
            f"{self.cp_service_url}/v1/production/revenue/metrics"
        )
        if revenue:
            total_usd = revenue.get("total_revenue_usd", 0)
            total_sales = revenue.get("total_sales", 0)
            fact = (
                f"Content Production revenue: ${total_usd:.2f} from "
                f"{total_sales} sales."
            )
            self.knowledge_base.add_knowledge(
                content=fact,
                knowledge_type=KnowledgeType.FACTUAL,
                source="content_production:revenue",
                confidence=0.9,
                context_tags=["content_production", "revenue"],
            )
            count += 1

        # Register as discoverable resource
        resource = DiscoveredResource(
            id="content-production-line",
            name="Content Production",
            resource_type=ResourceType.API,
            source=ResourceSource.CONTENT_PRODUCTION,
            description=(
                "Autonomous ebook production pipeline. Generates full ebooks "
                "(DOCX, HTML, PDF, Markdown) with 60+ marketing assets per topic. "
                "10 author personas, per-chapter KH enrichment, quality gates, "
                "multi-marketplace distribution (Gumroad, Etsy, Shopify, Amazon KDP), "
                "revenue tracking, 23 pre-production roadmaps (~730 chapters)."
            ),
            url=self.cp_service_url,
            capabilities=[
                "ebook_generation",
                "blueprint_generation",
                "research_with_authority_scoring",
                "marketing_asset_generation",
                "quality_evaluation",
                "revenue_tracking",
                "marketplace_distribution",
                "author_persona_matching",
                "per_chapter_kh_enrichment",
                "pdf_docx_html_output",
                "proactive_topic_scanning",
                "per_customer_distribution",
            ],
            tags=[
                "content-production", "ebooks", "marketing", "revenue",
                "distribution", "personas", "blueprints", "quality-gates",
            ],
            is_available=status is not None,
        )
        self.resource_discovery.register_resource(resource)

        logger.info("Content Production discovery: %d knowledge items", count)
        return count
