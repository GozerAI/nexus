"""
Discovery adapters for autonomous intelligence sources.

Wraps TrendSignalCollector, KHGraphCollector, and Content Production Line
as discovery sources so the hourly refresh cycle populates the Nexus
knowledge base with their latest output.

C-Suite reads from Nexus KB, so this makes it aware of work product
generated by all three autonomous systems.
"""

import logging
import os
from typing import Any, Dict, Optional

from .resource_discovery import (
    DiscoveredResource,
    ResourceDiscovery,
    ResourceSource,
    ResourceType,
)

logger = logging.getLogger(__name__)


class TrendscopeDiscoverySource:
    """Wraps TrendSignalCollector as a discovery source.

    On each ``discover()`` call (hourly), fetches the latest trends,
    signals, and lifecycle data from Trendscope and writes them into
    the Nexus knowledge base.
    """

    def __init__(
        self,
        resource_discovery: ResourceDiscovery,
        knowledge_base: Any,
    ):
        self.resource_discovery = resource_discovery
        self.knowledge_base = knowledge_base
        self._collector = None

        resource_discovery.register_source(ResourceSource.TRENDSCOPE, self)
        logger.info("TrendscopeDiscoverySource registered")

    def _ensure_collector(self):
        if self._collector is None:
            from nexus.data.trend_signal_collector import TrendSignalCollector

            self._collector = TrendSignalCollector(
                knowledge_base=self.knowledge_base,
            )

    async def discover(self) -> int:
        """Collect latest Trendscope intelligence into Nexus KB."""
        self._ensure_collector()

        try:
            result = self._collector.collect_all()
        except Exception as exc:
            logger.warning("Trendscope discovery failed: %s", exc)
            return 0

        total = result.get("total_knowledge_items", 0)

        # Also register Trendscope as a discoverable API resource
        resource = DiscoveredResource(
            id="trendscope-api",
            name="Trendscope Market Intelligence",
            resource_type=ResourceType.API,
            source=ResourceSource.TRENDSCOPE,
            description=(
                "Autonomous market trend intelligence service. "
                "Collects trends from Google Trends, Reddit, Hacker News, "
                "Product Hunt. Detects anomalies, signals, and opportunities."
            ),
            url=os.environ.get("TRENDSCOPE_BASE_URL", "http://localhost:8002"),
            capabilities=[
                "trend_collection",
                "signal_detection",
                "anomaly_detection",
                "lifecycle_analysis",
                "opportunity_scoring",
            ],
            tags=["trendscope", "market-intelligence", "trends", "signals"],
            is_available=total > 0,
        )
        self.resource_discovery.register_resource(resource)

        logger.info(
            "Trendscope discovery: %d trends, %d signals, %d lifecycle items",
            result.get("trends_collected", 0),
            result.get("signals_collected", 0),
            result.get("lifecycle_collected", 0),
        )
        return total


